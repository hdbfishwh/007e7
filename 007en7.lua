local function YCJYFD_fake_script() -- TextButton.LocalScript 
	local script = Instance.new('LocalScript', TextButton)

	local Players = game:GetService("Players")
	local player = Players.LocalPlayer
	local button = script.Parent
	
	button.MouseButton1Click:Connect(function()
		local character = player.Character
		if not character or not character:FindFirstChild("HumanoidRootPart") then return end
	
		local myRoot = character.HumanoidRootPart
		local nearbyPlayers = {}
	
		for _, otherPlayer in pairs(Players:GetPlayers()) do
			if otherPlayer ~= player and otherPlayer.Character and otherPlayer.Character:FindFirstChild("HumanoidRootPart") then
				local dist = (otherPlayer.Character.HumanoidRootPart.Position - myRoot.Position).Magnitude
				if dist <= 100 then -- teleport to players within 100 studs
					table.insert(nearbyPlayers, otherPlayer)
				end
			end
		end
	
		if #nearbyPlayers > 0 then
			local randomPlayer = nearbyPlayers[math.random(1, #nearbyPlayers)]
			local targetRoot = randomPlayer.Character:FindFirstChild("HumanoidRootPart")
			if targetRoot then
				myRoot.CFrame = targetRoot.CFrame + Vector3.new(2, 0, 2) -- teleport beside them
			end
		else
			warn("No nearby players found to teleport to.")
		end
	end)
	
end
